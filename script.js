let currentLang = localStorage.getItem("siteLanguage") || "en";

const applyLanguage = (lang) => {
  const dict = translations[lang] || translations.en;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dict[key]) {
      element.textContent = dict[key];
    }
  });

  const langButtonText = document.getElementById("langBtnText");
  if (langButtonText) {
    langButtonText.textContent = lang === "en" ? "中文" : "English";
  }

  const brandName = document.getElementById("brandName");
  const heroName = document.getElementById("heroName");
  if (brandName) brandName.textContent = lang === "en" ? "Shenghan Gao" : "高胜寒";
  if (heroName) heroName.textContent = lang === "en" ? "Shenghan Gao" : "高胜寒";

  document.documentElement.lang = lang === "en" ? "en" : "zh";
  currentLang = lang;
  localStorage.setItem("siteLanguage", lang);
};

const initNavigation = () => {
  const navToggle = document.getElementById("navToggle");
  const mainNav = document.getElementById("mainNav");
  const header = document.getElementById("siteHeader");
  const page = document.body.dataset.page;

  document.querySelectorAll(".main-nav a").forEach((link) => {
    const href = link.getAttribute("href") || "";
    if ((page === "home" && href === "index.html") || href.includes(`${page}.html`)) {
      link.classList.add("active");
    }
    link.addEventListener("click", () => {
      mainNav?.classList.remove("open");
      navToggle?.classList.remove("open");
      navToggle?.setAttribute("aria-expanded", "false");
    });
  });

  navToggle?.addEventListener("click", () => {
    const isOpen = mainNav?.classList.toggle("open");
    navToggle.classList.toggle("open", Boolean(isOpen));
    navToggle.setAttribute("aria-expanded", Boolean(isOpen).toString());
  });

  const updateHeader = () => {
    if (window.scrollY > 18) {
      header?.classList.add("scrolled");
    } else {
      header?.classList.remove("scrolled");
    }
  };

  updateHeader();
  window.addEventListener("scroll", updateHeader, { passive: true });
};

const initReveal = () => {
  const elements = document.querySelectorAll(".reveal");
  if (!elements.length) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("in-view");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  elements.forEach((element) => observer.observe(element));
};

const initTimelineFilter = () => {
  const filterButtons = document.querySelectorAll(".filter-btn");
  const timelineItems = document.querySelectorAll(".timeline-item");
  if (!filterButtons.length || !timelineItems.length) return;

  filterButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const selected = button.dataset.filter || "all";
      filterButtons.forEach((btn) => btn.classList.remove("active"));
      button.classList.add("active");

      timelineItems.forEach((item) => {
        const shouldShow = selected === "all" || item.dataset.category === selected;
        item.classList.toggle("hidden", !shouldShow);
      });
    });
  });
};

const initLanguageButton = () => {
  const toggleBtn = document.getElementById("langToggleBtn");
  toggleBtn?.addEventListener("click", () => {
    applyLanguage(currentLang === "en" ? "zh" : "en");
  });
};

window.addEventListener("DOMContentLoaded", () => {
  applyLanguage(currentLang);
  initNavigation();
  initReveal();
  initTimelineFilter();
  initLanguageButton();
});
